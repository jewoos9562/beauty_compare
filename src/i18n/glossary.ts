const FINALS = '\0ㄱㄲㄳㄴㄵㄶㄷㄹㄺㄻㄼㄽㄾㄿㅀㅁㅂㅄㅅㅆㅇㅈㅊㅋㅌㅍㅎ';
const FIN_ROM = ['','k','k','k','n','n','n','t','l','l','l','l','l','l','l','l','m','p','p','t','t','ng','t','t','k','t','p','t'];

function romanize(text: string): string {
  let result = '';
  for (let i = 0; i < text.length; i++) {
    const code = text.charCodeAt(i);
    if (code >= 0xAC00 && code <= 0xD7AF) {
      const offset = code - 0xAC00;
      const init = Math.floor(offset / (21 * 28));
      const med = Math.floor((offset % (21 * 28)) / 28);
      const fin = offset % 28;
      result += (INIT_ROM[init] || '') + (MED_ROM[med] || '') + (FIN_ROM[fin] || '');
    } else {
      result += text[i];
    }
  }
  return result;
}

// Regex to detect remaining Korean characters (Hangul)
const HANGUL_RE = /[\uAC00-\uD7AF]+/g;

export function translateText(text: string, lang: Lang): string {
  if (lang === 'ko') return text;
  let result = text;
  for (const entry of GLOSSARY) {
    if (result.includes(entry.ko)) {
      result = result.replaceAll(entry.ko, entry[lang]);
    }
  }
  // For any remaining Korean: romanize for en/es, keep for ja/zh (they share some chars)
  if (lang === 'ja' || lang === 'zh') {
    // For CJK languages, romanize remaining Korean
    result = result.replace(HANGUL_RE, match => romanize(match));
  } else {
    // For Latin-script languages, romanize
    result = result.replace(HANGUL_RE, match => romanize(match));
  }
  // Clean up artifacts
  result = result.replace(/\s{2,}/g, ' ').trim();
  return result;
}
